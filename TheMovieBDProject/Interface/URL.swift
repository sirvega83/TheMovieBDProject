extension URL {
    static let getPopularMovies = mainURL.appending(path: "popular")
    
    static func getMovieCastCrew(id: Int) -> URL {
        mainURL.appending(path: "\(id)/credits")
    }
   
    static func getTrailerMovie(id: Int) -> URL {
        mainURL.appending(path: "\(id)/videos")
    }
    
    static func getYoutbueTrailer(key: String) -> URL {
        URL(string: "https://www.youtube.com/watch?v=\(key)")!
    }
   
}

